package com.oceanbase.ocp.task.dao;

import java.util.Optional;

import javax.persistence.LockModeType;

import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Lock;
import org.springframework.data.jpa.repository.Query;

import com.oceanbase.ocp.task.entity.SubtaskInstanceEntity;

public interface SubtaskInstanceRepository extends JpaRepository<SubtaskInstanceEntity, Long> {

    @Lock(LockModeType.PESSIMISTIC_WRITE)
    @Query("SELECT u FROM SubtaskInstanceEntity u WHERE u.id = ?1")
    Optional<SubtaskInstanceEntity> lockAndFindById(Long id);

}
